        	<div class="b"><h4>取消订单规则</h4>
<p>1.在您付款前都是可以自行取消订单的；您付款后商品处于“待出库”状态的商品您可以取消，但请勿随意自行取消订单。如果成功付款后您有取消订单的动作及需求，请您联系华为商城客服。</p>
<p>2.支付成功的数据反馈可能有短期延迟，若您确认您已支付成功，但系统仍存在“取消订单”按钮，请您勿点击，否则订单将会被取消。</p>

