			</div>
        	<div class="b"><h4>快递运输</h4>
<p>华为商城采用顺丰和EMS的优质快递服务，可实现全国覆盖，为您提供更优质、更快速的物流配送服务。</p>

<h4>配送范围</h4>
<p>华为商城的物流配送范围覆盖中国大陆地区，目前暂不为港、澳、台及海外地区提供配送服务。</p>
<p>您可以使用下面的方法，查询具体的配送区域。</p>
<p><b>顺丰速递配送范围查询</b></p>
<p>打开<a href="javascript:if(confirm('http://www.sf-express.com/cn/sc/  \n\nļδ Teleport Pro ȡأΪ ·ʼַõķΧ  \n\nҪӷϴ'))window.location='http://www.sf-express.com/cn/sc/'" tppabs="http://www.sf-express.com/cn/sc/" target="_blank" title="顺丰官网">顺丰官网</a>，在网页左上角的“收送范围查询”栏目选择您所在的地区，就可以查看顺丰速递在您所在地区的配送范围。</p>
 
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.8/image001.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.8/image001.jpg" alt="" /></p>

<p><b>邮政快递EMS配送范围</b></p>
<p>中国邮政快递EMS配送范围覆盖全国。</p>

<h4>收费标准</h4>
<p>金额不足200元的订单收取一定的运费，下订单时系统会自动计算运费，运费金额会在您订单“配送方式”处显示。满200元的订单是免运费的。</p>

<h4>送达时间</h4>
<p><b>1. 配货时间</b></p>
<p>正常情况下，华为商城会在您支付成功后的1到2个工作日内为您配货发货。在商品预售（预定）的情况下，发货日期以商品页面显示的发货日期为准（由于预售商品是分批发货的，发货日期可能会晚于商品页面显示的发货日期）。</p>
<p><b>2. 送达时间</b></p>
<p>顺丰速运配送，广东省内地区一般1到2天可以送到，广东省外（新疆、西藏、内蒙等偏远地区除外）一般2到7天可以送到。邮政快递EMS配送，一般3到7天可以送到。</p>
<p>商品具体送达时间您可以登录顺丰或邮政快递EMS官网对您所购商品的快递服务进行实时跟踪。</p>

<h4>商品配送情况查询</h4>
<p>您购买的商品发货后就会产生快递单号，您可以登陆顺丰官网或邮政快递EMS官网对您所购商品的配送情况进行实时跟踪。</p>

<p><b>顺丰官网商品配送情况查询</b></p>
<p>查询前请核对您手中的快递单号是否为顺丰快递单号：顺丰快递单号由12位数字组成，常以当地电话区号后3位开头。</p>
<p>打开<a href="javascript:if(confirm('http://www.sf-express.com/cn/sc/  \n\nļδ Teleport Pro ȡأΪ ·ʼַõķΧ  \n\nҪӷϴ'))window.location='http://www.sf-express.com/cn/sc/'" tppabs="http://www.sf-express.com/cn/sc/" target="_blank" title="顺丰官网">顺丰官网</a>，在网页左上角的“快件追踪”输入框中输入您的快递单号，然后输入验证码，点击“查询”按钮，便可查询到您所购商品的配送情况。</p>
 <p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.8/image002.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.8/image002.jpg" alt="" /></p>

<p><b>邮政快递EMS官网商品配送情况查询</b></p>
<p>查询前请核对您手中的快递单号是否为EMS快递单号：EMS快递单号开头和结尾2位均是字母，中间是9位数字。</p>

<p>打开邮政快递<a href="javascript:if(confirm('http://www.ems.com.cn/  \n\nļδ Teleport Pro ȡأΪ ·ʼַõķΧ  \n\nҪӷϴ'))window.location='http://www.ems.com.cn/'" tppabs="http://www.ems.com.cn/" target="_blank" title="EMS官网">EMS官网</a>，在网页左上角的输入框中输入您的快递单号与验证码，点击“查询”按钮，便可查询到您所购商品的配送情况。</p> 

<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.8/image003.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.8/image003.jpg" alt="" /></p>

<h4>运输环节问题处理</h4>
<p><b>1. 商品配送时间过长</b></p>
<p>您知道商品运单号之后，如果觉得商品运输速度偏慢或配送时间过长，您可以联系顺丰或邮政快递EMS的客服，咨询或督促其加快配送速度。</p>
<p>顺丰快递客户服务热线：4008 111 111</p>
<p>邮政快递EMS客户服务热线：11183</p>

<p><b>2. 其他问题</b></p>
<p>如果运输环节出现其他问题，我们会积极有效地跟快递公司疏通跟进，您也可以随时联系华为客服人员查询进度。</p>

