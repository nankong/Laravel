<p>1.&nbsp;在商城会员登陆页面点击“忘记密码”（如下图红框部分所示），商城网页会自动进入“找回密码”页面；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.2/image001.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.2/image001.jpg" alt="" /></p>
 
<p>2.&nbsp;在“找回密码”页面，输入您的华为商城账号（手机号码），如下图所示，然后点击“下一步”按钮；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.2/image008.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.2/image008.jpg" alt="" /></p>
 
<p>3.&nbsp;点击“获取验证码”按钮，您注册的手机便会收到华为商城发送的验证码，请将收到的验证码填写在“手机验证码”旁边的方框中（如下图所示）；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.2/image009.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.2/image009.jpg" alt="" /></p>
 
