        	<div class="b"><p>如果您忘记了密码，华为商城为您提供了找回密码的服务。请在忘记密码的页面中输入您的华为商城账号，并按照网页提示进行操作，便可找回您的密码，具体操作方式如下所示。</p>

<p><b>一、邮箱账号密码找回操作步骤</b>
&nbsp;&nbsp;<a class="#" title="#"><span>找回密码</span></a></p>

<p>1.&nbsp;在商城会员登陆页面点击“忘记密码”（如下图红框部分所示），商城网页会自动进入“找回密码”页面；</p>
 <p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.2/image001.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.2/image001.jpg" alt="" /></p>
 
<p>2.&nbsp;在“找回密码”页面，输入您的华为商城账号（电子邮箱账号），如下图所示，然后点击“下一步”按钮；
</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.2/image002.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.2/image002.jpg" alt="" /></p>



<p>3.&nbsp;按照网页提示输入验证码，然后点击“找回密码”按钮，如下图所示；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.2/image003.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.2/image003.jpg" alt="" /></p>

<p>4.&nbsp;商城会自动给您的邮箱发一封密码修改的邮件，并出现下图的提示；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.2/image004.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.2/image004.jpg" alt="" /></p>
 
<p>5.&nbsp;请登录邮箱查看邮件，并通过邮件提供的网址及时重新设置自己的账户密码；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.2/image005.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.2/image005.jpg" alt="" /></p>
 
<p>6.&nbsp;通过邮件提供的网址进入密码重置页面，请输入您的新密码，然后点击“提交”按钮；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.2/image006.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.2/image006.jpg" alt="" /></p>
 
<p>7.&nbsp;提交新密码后，系统会提示您找回密码成功，如下图所示；之后登录商城请使用新密码进行登录。</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.2/image007.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.2/image007.jpg" alt="" /></p>
 

