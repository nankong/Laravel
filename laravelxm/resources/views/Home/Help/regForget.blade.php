<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.2/image010.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.2/image010.jpg" alt="" /></p>
 
<p>5.&nbsp;提交新密码后，系统会提示您手机重置密码成功，如下图所示；之后登录商城请使用新密码进行登录。</p>
