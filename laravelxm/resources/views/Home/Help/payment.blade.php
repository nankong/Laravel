			</div>
        	<div class="b"><h4>网银支付流程</h4>
<p>1.如果您想使用网上银行（包括开通了网银功能的储蓄卡和信用卡）支付货款，请在“提交订单”页面选择“银行卡支付”，然后选择您网银所属的银行；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.12/01.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.12/01.jpg" alt="" /></p>
 
<p>例如，下图就选择了中国工商银行的网银作为支付方式；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.12/02.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.12/02.jpg" alt="" /></p>
 
<p>2.提交订单后，商城页面会提示您“立即支付”，请单击立即支付按钮；商城页面会自动进入您所选择银行的网银支付界面；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.12/03.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.12/03.jpg" alt="" /></p>
 
<p>3. 在网银支付界面，请按照网页提示进行操作；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.12/04.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.12/04.jpg" alt="" /></p>
 
<p>4.您支付成功后，银行会有相应的提醒；您支付成功后，请返回“支付”页面，选择“已完成付款”；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.12/05.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.12/05.jpg" alt="" /></p>
 
<p>5. 如果您在支付过程中遇到了问题，付款未成功，请请返回“支付”页面，选择“付款遇到问题”；您也可以与银行联系，咨询具体的操作方式；</p>
<p style="text-align:center;"><img src="../../res.vmall.com/20140801/images/help/M1.12/06.jpg" tppabs="http://res.vmall.com/20140801/images/help/M1.12/06.jpg" alt="" /></p>
 

